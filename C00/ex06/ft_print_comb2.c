#include <unistd.h>

void	write_x(int x)
{
	int	n;
	int	m;

	if (x <= 9)
	{
		n = x + 48;
		write(1, "0", 1);
		write(1, &n, 1);
	}
	else
	{
		n = (x / 10) + 48;
		m = (x % 10) + 48;
		write(1, &n, 1);
		write(1, &m, 1);
	}
}

void	ft_print_comb2(void)
{
	int	n[2];

	n[0] = 0;
	while (n[0] < 99)
	{
		n[1] = n[0] + 1;
		while (n[1] < 100)
		{
			write_x(n[0]);
			write(1, " ", 1);
			write_x(n[1]);
			if (n[0] + n[1] != 197)
			{
				write(1, ", ", 2);
			}
			n[1]++;
		}
		n[0]++;
	}
}
