int	ft_sqrt(int nb)
{
	int	num;

	if (nb < 0)
		return (0);
	num = 1;
	if (nb > 2147395600)
		return (0);
	while (num * num <= nb)
	{
		if (num * num == nb)
			return (num);
		num++;
	}
	return (0);
}
