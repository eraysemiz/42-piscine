void	ft_rev_int_tab(int *tab, int size)
{
	int	tmp;
	int	x;
	int	y;

	x = 0;
	y = size - 1;
	while (x < size / 2)
	{
		tmp = tab[x];
		tab[x] = tab[y];
		tab[y] = tmp;
		x++;
		y--;
	}
}
